"use strict";

const path = require("path");

const { cmdStage } = require("../../scripts/sync/sync-gate.js");
const notionConfig = require("../notion/notion.config.js");
const {
  OUTPUT_FILES,
  ensureBaseDirs,
  getLatestRunId,
  nowIso,
  parseArgs,
  readJsonl,
  relativeToProject,
  readJson,
  writeJson,
} = require("../core/shared");

function groupBySource(rows) {
  return rows.reduce((acc, row) => {
    acc[row.source_name] = acc[row.source_name] || [];
    acc[row.source_name].push(row);
    return acc;
  }, {});
}

function getDiscoverSummary(runId) {
  const reportPath = path.join(path.dirname(OUTPUT_FILES.source_records), `discover_report_${runId}.json`);
  const report = readJson(reportPath, null);
  return {
    reportPath,
    summary: Array.isArray(report?.summary) ? report.summary : [],
  };
}

async function runNotionSync(options = {}) {
  ensureBaseDirs();
  const args = options.args || parseArgs(process.argv.slice(2));
  const runId = options.runId || args["run-id"] || getLatestRunId(OUTPUT_FILES.source_records);
  if (!runId) {
    throw new Error("No run available for Notion sync preview.");
  }

  const sourceRecords = readJsonl(OUTPUT_FILES.source_records).filter((row) => row.run_id === runId);
  const normalizedRecords = readJsonl(OUTPUT_FILES.normalized_records).filter((row) => row.run_id === runId);
  const matches = readJsonl(OUTPUT_FILES.match_candidates).filter((row) => row.run_id === runId);
  const assets = readJsonl(OUTPUT_FILES.external_assets).filter((row) => row.run_id === runId);
  const reviewRows = readJsonl(OUTPUT_FILES.review_queue).filter((row) => row.run_id === runId);
  const discover = getDiscoverSummary(runId);

  const sources = groupBySource(sourceRecords);
  const discoverSources = discover.summary.map((item) => {
    const insertedRows = sources[item.source] || [];
    return {
      name: item.source,
      source_type: notionConfig.sourceTypes[item.source] || "unknown",
      status: item.error ? "error" : "preview",
      last_crawl: insertedRows[insertedRows.length - 1]?.created_at || nowIso(),
      records_found: item.stats?.total_records ?? item.inserted ?? 0,
      errors_count: item.error ? 1 : 0,
      priority: notionConfig.defaultPriority[item.source] || "medium",
      notes: item.error
        ? `Preview payload generated with source error: ${item.error}`
        : "Preview payload generated by Polish RetroDex v1.",
    };
  });
  const sourceNames = discoverSources.map((item) => item.name);
  const recordsProcessed = discoverSources.reduce((acc, item) => acc + (item.records_found || 0), 0);

  const summaryPayload = {
    run_id: runId,
    generated_at: nowIso(),
    targets: notionConfig.targets,
    sources: discoverSources,
    ingestion_runs: [
      {
        run_id: runId,
        date: nowIso(),
        stage: "pipeline",
        source: sourceNames.join(", "),
        records_processed: recordsProcessed,
        success_count: assets.length,
        reject_count: matches.filter((row) => row.match_status === "rejected").length,
        review_count: reviewRows.length,
        duration: null,
        status: "preview",
        log_path: relativeToProject(discover.reportPath),
      },
    ],
    review_queue: reviewRows.slice(0, 100),
    assets_ready: assets.slice(0, 100),
    coverage_dashboard: {
      records_by_source: Object.fromEntries(discoverSources.map((item) => [item.name, item.records_found])),
      normalization_ratio: sourceRecords.length ? Number((normalizedRecords.length / sourceRecords.length).toFixed(3)) : 0,
      auto_match_ratio: matches.length ? Number((matches.filter((row) => row.match_status === "auto_matched").length / matches.length).toFixed(3)) : 0,
      review_ratio: matches.length ? Number((reviewRows.length / matches.length).toFixed(3)) : 0,
      validated_asset_count: assets.length,
    },
  };

  const previewPath = path.join(path.dirname(OUTPUT_FILES.source_records), `notion_preview_${runId}.json`);
  writeJson(previewPath, summaryPayload);

  if (args.stage || options.stage) {
    const syncPayload = {
      session: `PRD ${runId}`,
      status: "review",
      date: new Date().toISOString().slice(0, 10),
      area: "Polish RetroDex",
      summary: `Preview staged for ${sourceRecords.length} source records, ${assets.length} ready assets, ${reviewRows.length} review items.`,
    };
    cmdStage("sync_log", JSON.stringify(syncPayload));
  }

  return {
    runId,
    previewPath,
    staged: Boolean(args.stage || options.stage),
  };
}

if (require.main === module) {
  runNotionSync().then((result) => {
    console.log(JSON.stringify({
      ok: true,
      run_id: result.runId,
      preview_file: relativeToProject(result.previewPath),
      staged: result.staged,
    }, null, 2));
  }).catch((error) => {
    console.error(error.message);
    process.exit(1);
  });
}

module.exports = {
  runNotionSync,
};
